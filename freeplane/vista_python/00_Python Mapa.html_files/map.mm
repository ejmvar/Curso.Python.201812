</font>
</a>      </h2>
      <p style="line-height: 1.4em">
        <font size="2">When choosing a Python interpreter, one looming question is always present: &#8220;Should I choose Python 2 or Python 3&#8221;? The answer is a bit more subtle than one might think. </font>
      </p>
      <p style="line-height: 1.4em">
        <font size="2">The basic gist of the state of things is as follows: </font>
      </p>
      <ol class="arabic simple" style="margin-top: 10px; margin-right: 0px; margin-bottom: 10px; margin-left: 30px; padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; list-style: decimal">
        <li style="line-height: 1.4em">
          <font size="2">Most production applications today use Python 2.7. </font>
        </li>
        <li style="line-height: 1.4em">
          <font size="2">Python 3 is ready for the production deployment of applications today. </font>
        </li>
        <li style="line-height: 1.4em">
          <font size="2">Python 2.7 will only receive necessary security updates until 2020&#160;</font>
<a class="footnote-reference" href="https://docs.python-guide.org/starting/which-python/#pep373-eol" id="id1" style="color: rgb(0, 75, 107); text-decoration: none; font-size: 0.7em; vertical-align: top">
<font color="rgb(0, 75, 107)" size="0.7em">[6]</font>
</a>
<font size="2">. </font>
        </li>
        <li style="line-height: 1.4em">
          <font size="2">The brand name &#8220;Python&#8221; encapsulates both Python 3 and Python 2. </font>
        </li>
      </ol>
    </div>
