      <div class="admonition note" style="margin-top: 20px; margin-bottom: 20px; margin-right: 0px; margin-left: 0px; padding-top: 10px; padding-bottom: 10px; padding-right: 30px; padding-left: 30px; background-color: rgb(255, 245, 156)">
        <p class="first admonition-title" style="margin-top: 0px; margin-right: 0px; margin-bottom: 10px; margin-left: 0px; font-weight: 400; display: inline; line-height: 1; font-family: Georgia, serif; font-size: 24px; padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px">
          Note
        </p>
        <p class="last" style="line-height: 1.4em; margin-bottom: 0px">
          <font size="1">The use of&#160;<strong>Python 3</strong>&#160;is&#160;<em>highly</em>&#160;preferred over Python 2. Consider upgrading your applications and infrastructure if you find yourself&#160;<em>still</em>&#160;using Python 2 in production today. If you are using Python 3, congratulations &#8212;&#160;you are indeed a person of excellent taste. &#8212;<em>Kenneth Reitz</em>
</font>
        </p>
      </div>
      <p style="line-height: 1.4em">
        <font size="2">I&#8217;ll be blunt: </font>
      </p>
      <ul class="simple" style="margin-top: 10px; margin-right: 0px; margin-bottom: 10px; margin-left: 30px; padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px">
        <li style="line-height: 1.4em">
          <font size="2">Use Python 3 for new Python applications. </font>
        </li>
        <li style="line-height: 1.4em">
          <font size="2">If you&#8217;re learning Python for the first time, familiarizing yourself with Python 2.7 will be very useful, but not more useful than learning Python 3. </font>
        </li>
        <li style="line-height: 1.4em">
          <font size="2">Learn both. They are both &#8220;Python&#8221;. </font>
        </li>
        <li style="line-height: 1.4em">
          <font size="2">Software that is already built often depends on Python 2.7. </font>
        </li>
        <li style="line-height: 1.4em">
          <font size="2">If you are writing a new open source Python library, it&#8217;s best to write it for both Python 2 and 3 simultaneously. Only supporting Python 3 for a new library you want to be widely adopted is a political statement and will alienate many of your users. This is not a problem &#8212; slowly, over the next three years, this will become less the case. </font>
        </li>
      </ul>
