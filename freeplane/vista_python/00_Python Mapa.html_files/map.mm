      <h2 style="font-family: Georgia, serif; font-weight: 400; margin-top: 30px; margin-right: 0px; margin-bottom: 10px; margin-left: 0px; padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; font-size: 30.6px">
        Recommendations<a class="headerlink" href="https://docs.python-guide.org/starting/which-python/#recommendations" title="Permalink to this headline" style="color: rgb(221, 221, 221); text-decoration: none; padding-top: 0px; padding-bottom: 0px; padding-right: 4px; padding-left: 4px">
