        <font size="3">
<b>So&#8230;. 3?</b>
</font>
<a class="headerlink" href="https://docs.python-guide.org/starting/which-python/#so-3" title="Permalink to this headline" style="color: rgb(221, 221, 221); text-decoration: none; padding-top: 0px; padding-bottom: 0px; padding-right: 4px; padding-left: 4px">
<font color="rgb(221, 221, 221)">
</font>
</a>      </h2>
      <p style="line-height: 1.4em">
        <font size="2">If you&#8217;re choosing a Python interpreter to use, I recommend you use the newest Python 3.x, since every version brings new and improved standard library modules, security and bug fixes. </font>
      </p>
      <p style="line-height: 1.4em">
        <font size="2">Given such, only use Python 2 if you have a strong reason to, such as a pre-existing code-base, a Python 2 exclusive library, simplicity/familiarity, or, of course, you absolutely love and are inspired by Python 2. No harm in that. </font>
      </p>
      <p style="line-height: 1.4em">
        <font size="2">Check out&#160;</font>
<a class="reference external" href="https://caniusepython3.com/" style="color: rgb(0, 75, 107); text-decoration: none">
<font color="rgb(0, 75, 107)" size="2">Can I Use Python 3?</font>
</a>
<font size="2">&#160;to see if any software you&#8217;re depending on will block your adoption of Python 3. </font>
      </p>
      <p style="line-height: 1.4em">
        <a class="reference external" href="http://wiki.python.org/moin/Python2orPython3" style="color: rgb(0, 75, 107); text-decoration: none">
<font color="rgb(0, 75, 107)" size="2">Further Reading</font>
</a>
      </p>
      <p style="line-height: 1.4em">
        <font size="2">It is possible to&#160;</font>
<a class="reference external" href="https://docs.python.org/3/howto/pyporting.html" style="color: rgb(0, 75, 107); text-decoration: none">
<font color="rgb(0, 75, 107)" size="2">write code that works on Python 2.6, 2.7, and Python 3</font>
</a>
<font size="2">. This ranges from trivial to hard depending upon the kind of software you are writing; if you&#8217;re a beginner there are far more important things to worry about. Note that Python 2.6 is end-of-life upstream, so you shouldn&#8217;t try to write 2.6-compatible code unless you&#8217;re being paid specifically to do that.</font>
      </p>
    </div>
    <div class="section" id="implementations" style="color: rgb(62, 67, 73); font-family: Georgia, serif; font-size: 17px; font-style: normal; font-weight: 400; letter-spacing: normal; text-align: left; text-indent: 0px; text-transform: none; white-space: normal; word-spacing: 0px; background-color: rgb(255, 255, 255)">
