        The State of Python (3 &amp; 2)<a class="headerlink" href="https://docs.python-guide.org/starting/which-python/#the-state-of-python-3-2" title="Permalink to this headline" style="color: rgb(221, 221, 221); text-decoration: none; padding-top: 0px; padding-bottom: 0px; padding-right: 4px; padding-left: 4px">
